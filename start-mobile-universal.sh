#!/bin/bash

# Универсальный скрипт для запуска мобильной версии приложения
# Автоматически определяет IP-адрес и запускает сервер для мобильного тестирования

echo "🚀 Запуск универсальной мобильной версии приложения..."
echo ""

# Получаем IP-адрес
IP=$(ifconfig | grep "inet " | grep -v 127.0.0.1 | head -1 | awk '{print $2}')

echo "📱 Мобильная версия будет доступна по адресам:"
echo "   http://$IP:3000"
echo "   http://192.168.1.13:3000"
echo "   http://192.168.1.196:3000"
echo "   http://192.168.1.192:3000"
echo ""
echo "💻 Локальная версия:"
echo "   http://localhost:3000"
echo ""

# Проверяем, свободен ли порт
if lsof -Pi :3000 -sTCP:LISTEN -t >/dev/null ; then
    echo "⚠️  Порт 3000 занят. Останавливаем процессы..."
    lsof -ti:3000 | xargs kill -9
    sleep 2
fi

echo "🔄 Запускаем мобильный сервер..."
echo ""

# Проверяем зависимости
if [ ! -d "node_modules" ]; then
    echo "📦 Устанавливаем зависимости..."
    npm install
    echo ""
fi

echo "📱 Запуск сервера для мобильных устройств..."
echo "⚠️  Для остановки нажмите Ctrl+C"
echo ""
echo "📋 Инструкции для подключения с телефона:"
echo "   1. Убедитесь, что телефон подключен к той же Wi-Fi сети"
echo "   2. Откройте браузер на телефоне"
echo "   3. Перейдите по адресу: http://$IP:3000"
echo ""

# Запускаем мобильную версию
npm run dev:mobile

