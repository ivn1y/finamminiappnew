#!/bin/bash

# Скрипт для запуска полной локальной среды разработки
# Включает в себя mock-api сервер и основной Next.js сервер

echo "🚀 Запуск полной среды разработки..."
echo ""

# Запускаем mock API сервер в фоновом режиме
echo "🔥 Запуск mock API сервера..."
npm run mock-api &
MOCK_API_PID=$!
echo "✅ Mock API сервер запущен с PID $MOCK_API_PID."
echo ""

# Устанавливаем обработчик сигнала для остановки mock API сервера
# Этот trap сработает, когда скрипт получит сигнал INT (например, от Ctrl+C)
# Это гарантирует, что фоновый процесс mock-api будет завершен.
trap "echo '🛑 Остановка серверов...'; kill $MOCK_API_PID 2>/dev/null; exit" INT

# Запускаем основной сервер приложения для мобильных и десктопных устройств
# Скрипт 'npm run mobile' выведет IP-адрес для доступа с мобильного устройства
npm run mobile

# Когда пользователь остановит 'npm run mobile' (например, через Ctrl+C),
# сработает 'trap', который остановит mock-api сервер.
